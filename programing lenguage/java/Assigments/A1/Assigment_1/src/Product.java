public class Product {
    String productName;
    int price;
    double protein;
    double carbohydrate;
    double fat;
    double calorie;

    public Product (
            String productName,
            int price,
            double protein,
            double carbohydrate,
            double fat,
            double calorie){

        this.productName = productName;
        this.price = price;
        this.protein = protein;
        this.carbohydrate = carbohydrate;
        this.fat = fat;
        this.calorie = calorie;
    }





}
