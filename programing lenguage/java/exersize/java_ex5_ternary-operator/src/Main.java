public class Main {
    public static void main(String[] args) {
        System.out.println("Ternary operator");


        int a = 5, b = 9;
        int result = a + b ;
        String y = result >= 10 ?  "true" :  "false" ;
        System.out.println(y);

        y = result == 10 ?  "true" :  "false" ;
        System.out.println(y);
    }
}